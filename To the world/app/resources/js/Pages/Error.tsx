interface Props {
    status: number;
}

type StatusContent = {
    titleKey: string;
    descriptionKey: string;
    Illustration: React.ComponentType<React.ComponentPropsWithoutRef<'svg'>>;
    showRefresh: boolean;
};

function contentFor(status: number): StatusContent {
    switch (status) {
        case 403:
            return {
                titleKey: '403.title',
                descriptionKey: '403.description',
                Illustration: ForbiddenIllustration,
                showRefresh: false,
            };
        case 404:
            return {
                titleKey: '404.title',
                descriptionKey: '404.description',
                Illustration: NotFoundIllustration,
                showRefresh: false,
            };
        case 500:
            return {
                titleKey: '500.title',
                descriptionKey: '500.description',
                Illustration: ServerErrorIllustration,
                showRefresh: true,
            };
        case 503:
            return {
                titleKey: '503.title',
                descriptionKey: '503.description',
                Illustration: ServiceUnavailableIllustration,
                showRefresh: true,
            };
        default:
            return {
                titleKey: 'default.title',
                descriptionKey: 'default.description',
                Illustration: ServerErrorIllustration,
                showRefresh: true,
            };
    }
}

export default function Error({ status }: Props) {
    const { t } = useTranslation('errors');
    const { titleKey, descriptionKey, Illustration, showRefresh } = contentFor(status);

    return (
        <>
            <Head title={t(titleKey)} />
            <div className={classes.page}>
                <header className={classes.header}>
                    <a href="/" className={classes.logoLink}>
                        <Logo />
                    </a>
                    <LanguageSwitcher />
                </header>

                <Container className={classes.root}>
                    <div className={classes.inner}>
                        <Illustration className={classes.image} />

                        <div className={classes.content}>
                            <Title className={classes.title}>{t(titleKey)}</Title>
                            <Text c="dimmed" size="lg" ta="center" className={classes.description}>
                                {t(descriptionKey)}
                            </Text>
                            <Group justify="center">
                                <Button component="a" href="/" size="md">
                                    {t('backHome')}
                                </Button>
                                {showRefresh && (
                                    <Button
                                        variant="default"
                                        size="md"
                                        onClick={() => window.location.reload()}
                                    >
                                        {t('refresh')}
                                    </Button>
                                )}
                            </Group>
                        </div>
                    </div>
                </Container>
            </div>
        </>
    );
}
